void KeyboardHandler(Registers_t regs)
{
    unsigned char scancode;

    scancode = inb(0x60);

    if (scancode & 0x80)
    {

    }
    else
    {
        MonitorPutc(kbdus[scancode]);
    }
    
}

void InstallKeyboard()
{
    RegisterInterruptHandler(IRQ1, KeyboardHandler);
}